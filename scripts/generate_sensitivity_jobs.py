"""Generate TC1 sbatch files for the T18 multi-seed sensitivity arm.

Emits, into ``slurm/jobs_tc1_sensitivity/``:

* one *generation* sbatch per model (``<alias>__sens.sbatch``) that loops over
  the seed set, re-running HarmBench under stochastic decoding (T=0.7, from
  ``configs/tc1_sensitivity.yaml``) into ``results_sensitivity/seed<N>/``; and
* one *judge* sbatch per seed (``judge_sens_seed<N>.sbatch``) that scores that
  seed's generations with the official HarmBench classifier, so the sensitivity
  metric matches the judge-primary headline.

The generation jobs request the same modest resources as the main matrix; the
judge jobs mirror ``slurm/judge_validation.sbatch`` (fp16, 40 GB, batch 4). All
log paths are absolute, so the files are safe to ``sbatch`` from any directory.

This is a deterministic code-generation step (no GPU, no network); run it on the
Mac and commit the result, then ``git pull`` on TC1 and ``sbatch`` directly
(TC1 policy forbids running Python on the head node).
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import List

WORK_DIR = "/tc1home/FYP/utan001/fyp_quant/repo"
SENS_CONFIG = "configs/tc1_sensitivity.yaml"
SENS_ROOT = "results_sensitivity"
LOG_DIR = "results/slurm_logs_tc1"

# Grouped by pair so the user can run the load-bearing Qwen 1.7B pair first.
MODELS_BY_PAIR = [
    ("qwen_2b", ["qwen_2b_base", "qwen_2b_4bit"]),
    ("qwen_4b", ["qwen_4b_base", "qwen_4b_4bit"]),
    ("llama_3_2_3b", ["llama_3_2_3b_base", "llama_3_2_3b_4bit"]),
]
ALL_MODELS = [alias for _, aliases in MODELS_BY_PAIR for alias in aliases]

BOOTSTRAP = f"""set -euo pipefail

cd {WORK_DIR}
mkdir -p {LOG_DIR} {SENS_ROOT}
module load slurm
module load anaconda
source activate fyp-tc1
export HF_HUB_OFFLINE=1
export HF_DATASETS_OFFLINE=1
export TRANSFORMERS_OFFLINE=1
"""


def _gen_sbatch(alias: str, seeds: List[int]) -> str:
    seed_list = " ".join(str(s) for s in seeds)
    return f"""#!/bin/bash
#SBATCH --job-name={alias}__sens
#SBATCH --partition=UGGPU-TC1
#SBATCH --qos=normal
#SBATCH --gres=gpu:1
#SBATCH --cpus-per-task=1
#SBATCH --mem=10G
#SBATCH --time=06:00:00
#SBATCH --output={WORK_DIR}/{LOG_DIR}/{alias}__sens.out
#SBATCH --error={WORK_DIR}/{LOG_DIR}/{alias}__sens.err

{BOOTSTRAP}
# T18 sensitivity: HarmBench under stochastic decoding (T=0.7) across seeds.
# Each seed writes to a separate root so resume logic does not collide.
for SEED in {seed_list}; do
  echo "=== {alias} seed $SEED (T=0.7 HarmBench sensitivity) ==="
  python {WORK_DIR}/run_quant_matrix.py \\
    --config {SENS_CONFIG} \\
    --model {alias} \\
    --benchmark harmbench \\
    --seed "$SEED" \\
    --output_dir {SENS_ROOT}/seed"$SEED" \\
    --device cuda
done
"""


def _judge_sbatch(seed: int, models: List[str]) -> str:
    model_args = " ".join(models)
    return f"""#!/bin/bash
#SBATCH --job-name=judge_sens_seed{seed}
#SBATCH --partition=UGGPU-TC1
#SBATCH --qos=normal
#SBATCH --gres=gpu:1
#SBATCH --cpus-per-task=2
#SBATCH --mem=40G
#SBATCH --time=05:00:00
#SBATCH --output={WORK_DIR}/{LOG_DIR}/judge_sens_seed{seed}.out
#SBATCH --error={WORK_DIR}/{LOG_DIR}/judge_sens_seed{seed}.err

{BOOTSTRAP}
# Score this seed's stochastic HarmBench generations with the official
# HarmBench classifier (fp16), so the sensitivity ASR matches the judge-primary
# headline metric. Requires the classifier cached on the HEAD node first:
#   python scripts/prefetch_tc1.py --config configs/tc1.yaml --judge
# Edit --models below to match the models you actually generated for this seed.
python {WORK_DIR}/scripts/run_judge_validation.py \\
  --results-dir {SENS_ROOT}/seed{seed} \\
  --models {model_args} \\
  --benchmark harmbench \\
  --backend harmbench_cls \\
  --precision fp16 \\
  --batch-size 4 \\
  --device cuda
"""


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate T18 sensitivity sbatch files.")
    parser.add_argument("--seeds", nargs="+", type=int, default=[1, 2, 3, 4, 5])
    parser.add_argument("--out-dir", default="slurm/jobs_tc1_sensitivity")
    args = parser.parse_args()

    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    written: List[str] = []
    for alias in ALL_MODELS:
        path = out_dir / f"{alias}__sens.sbatch"
        path.write_text(_gen_sbatch(alias, args.seeds), encoding="utf-8")
        written.append(str(path))
    for seed in args.seeds:
        path = out_dir / f"judge_sens_seed{seed}.sbatch"
        path.write_text(_judge_sbatch(seed, ALL_MODELS), encoding="utf-8")
        written.append(str(path))

    readme = out_dir / "README.md"
    readme.write_text(
        "# T18 sensitivity-arm sbatch files (generated)\n\n"
        "Generated by `scripts/generate_sensitivity_jobs.py` — do not hand-edit; "
        "regenerate instead. Seeds: " + ", ".join(str(s) for s in args.seeds) + ".\n\n"
        "Run order on TC1 (one GPU job at a time):\n"
        "1. Generation, load-bearing pair first: `sbatch slurm/jobs_tc1_sensitivity/qwen_2b_base__sens.sbatch` "
        "then `qwen_2b_4bit__sens.sbatch` (optionally the qwen_4b and llama pairs for full symmetry).\n"
        "2. After generation for a seed completes, judge it: `sbatch slurm/jobs_tc1_sensitivity/judge_sens_seed1.sbatch` "
        "(repeat per seed; edit `--models` if you only generated one pair).\n"
        "3. SCP `results_sensitivity/` back to the Mac and run "
        "`python scripts/sensitivity_analysis.py`.\n",
        encoding="utf-8",
    )
    written.append(str(readme))

    print(f"Wrote {len(written)} files to {out_dir}/:")
    for path in written:
        print(f"  {path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
